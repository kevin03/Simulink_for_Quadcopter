function [ max_L ] = Tune( F )
clear G Gc;
for w1 =0:10000; 
    w = w1/1000; 
    G(1:3,1:3,w1+1) = [-0.26978*exp(-27.5*j*w)./(97.5*j*w+1) 1.978*exp(-53.5*j*w)./(118.5*j*w+1) 0.07724*exp(-56*j*w)./(96*j*w+1); 
        0.4881*exp(-117*j*w)./(56*j*w+1) 5.26*exp(-265*j*w)./(58.5*j*w+1) 0.19996*exp(-35*j*w)./(51*j*w+1); 
        -0.6*exp(-165*j*w)./(40.5*j*w+1) 5.5*exp(-15.5*j*w)./(19.5*j*w+1) -0.5*exp(-17*j*w)./(18*j*w+1)];
end

for w1 =0:10000;
    w = w1/1000;
    Gc1 = -7.301/F*(1+1/F/97.5/j./w);
    Gc2 = 0.02332/F*(1+1/F/58.5/j./w);
    Gc3 = -1.176/F*(1+1/F/18/j./w);
    Gc(1:3,1:3,w1+1) = [Gc1 0 0;0 Gc2 0;0 0 Gc3];
end;

for w1 = 0:10000;
    W(w1+1) = -1 + det([1 0 0;0 1 0;0 0 1]+G(1:3,1:3,w1+1)*Gc(1:3,1:3,w1+1));
end

L = 20*log10(abs(W./(W+1)));
max_L = max(L);
end

